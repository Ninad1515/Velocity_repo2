package loop;

public class Forloop4 {

	public static void main(String[] args) {
		// write a program tp print 5 to 1 numbers using for loop
         //starting point=5
        // ending point=1
        // inc/dec
		
		
		
		
		for(int a=5;a>=1;a--) {
			System.out.println(a);
			
		}
		System.out.println("Loop completed");
		
	}

}
