package loop;

public class Forloop2 {

	public static void main(String[] args) {
		//write the program to print 1 to 20 numbers using loop
		//starting point =1
        //ending point =20
		
		for(int N=1;N<=20;N++) {
			System.out.println(N);
			
		}
		System.out.println("Program completed");
		
	}

}
