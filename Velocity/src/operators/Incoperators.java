package operators;

public class Incoperators {

	public static void main(String[] args) {
		     int a = 10;
		     int b = 20 ;
		     int c;
		     
		     c=a; //assign operator
		     System.out.println(c);
		     
		     a++; // inc by 1
		     System.out.println(a);
		     
		     c=c+1;
		     System.out.println(c);
		     
		     c=c+2;
		     System.out.println(c);
		     
		     c=c+3;
		     System.out.println(c);
		     
		     c--;
		     System.out.println(c);
		        
		     c=c-1;
		     System.out.println(c);
		     
		     c=c-2;
		     System.out.println(c);
		        
		        
		        
	}

}
