package march;

public class chardatatype {

	public static void main(String[] args) {
		//char data type
		char c1 = 'A';
		char c2 = '&';
		char c3 = '<';
		char c4 = '$';
		
		System.out.println("char data type="+c1);
		System.out.println("char data type="+c2);
		System.out.println("char data type="+c3);
		System.out.println("char data type="+c4);
		System.out.println("************************");
		
		// Boolean data type=1 Bit
		boolean b1 = true;
		boolean b2 = false;
		
		System.out.println("boolean data type="+b1);
		System.out.println("boolean data type="+b2);
		
		
	}

}
